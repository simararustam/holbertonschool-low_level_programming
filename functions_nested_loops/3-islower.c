#include "main.h"

/**
 * _islower - entry point
 * Description: This function prints the alphabet, in lowercase
 * @c: entry parametr
 * main - check the code
 * Return: Always 0
 */

int _islower(int c)
{

	if (c >= 'a' && c <= 'z')
	{
	_putchar('0');
	}
	else if (c >= 'A' && c <= 'Z')
	{
	_putchar('0');
	}

	return (0);
}
