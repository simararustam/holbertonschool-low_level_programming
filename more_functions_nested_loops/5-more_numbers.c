#include <stdio.h>
/**
 * more_numbers - entry point
 * Description: This function prints the alphabet, in lowercase
 * main - check the code
 */

void more_numbers(void)
{
	int i, j;

		for (i = 0; i < 10; i++)
		{
			for (j = 0; j <= 14; j++)
			{
				printf("%d", j);
			}
		_putchar('\n');
		}
}
