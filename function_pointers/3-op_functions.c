#include <stdio.h>
#include "3-calc.h"
/**
 * op_add - add
 * @a: a
 * @b: b
 * Return: value
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod
 * @a: a
 * @b: b
 * Return: value
 */
int op_mod(int a, int b)
{
	return (a % b);
}
